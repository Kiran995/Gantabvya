public class Routes {

    int id;
    String name;

    Routes (int id, String name){
        this.id = id;
        this.name = name;

    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public int setId() {
        return id;
    }

    public String setName() {
        return name;
    }
}